public class Arithmeticoperators
 { 
   public static void main(String[]args)
     {
       // arithmetic operators(+,-,*,/)
       //Addition
       int a=10;
       int b=5;
       int c=a+b;
       System.out.println(c);

       //Subraction
       int d=a-b;
       System.out.println(d);
         
       //Multiplication
       int e=a*b;
       System.out.println(e);
 
       //Divison
       int f=a/b;
       System.out.println(f);
     }
  }